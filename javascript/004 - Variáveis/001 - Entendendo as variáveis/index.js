/* 
    var - ele é usado globalmente
    let - ele é usado dentro do escopo
    const - ele é usado dentro do escopo porém podemos armazenar dados apenas 1x
*/

var cachorro = 'Tarcius';
console.log(cachorro)

cachorro = 'Teca'
console.log(cachorro)

let lanche = 'Bolovo';
console.log(lanche)

lanche = 'Tapioca'
console.log(lanche)

const NOME = 'Dener';
console.log(NOME)